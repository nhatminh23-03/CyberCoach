        f"**{translations['risk_level']}:** {_markdown_text(payload['risk_label'])}",
        f"**{translations['confidence']}:** {_markdown_text(payload['confidence'])}",
        f"**{translations['likely_pattern']}:** {_markdown_text(payload['likely_scam_pattern'])}",
